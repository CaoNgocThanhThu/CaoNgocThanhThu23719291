a = int(input("Nhập số nguyên a: "))
b = int(input("Nhập số nguyên b: "))
print("Tổng a + b: ", a + b)
print(" Hiệu a - b: ", a - b)
print("Tích a x b: ", a * b)
print("Thương a / b: ", round(a / b, 3))
print("Chia lấy dư a % b: ", a % b)
print("Chia lấy nguyên a // b: ", a // b)